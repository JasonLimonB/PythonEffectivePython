# Tenemos los mismo operadores aritmeticos  que en otros lenguajes de porgramacion pero un repaso no viene mal

print(f'Suma {5 + 5}')
print(f'Resta {10 - 1}')
print(f'Multiplicacion {5 * 4}')
print(f'Division {100 / 5}')
print(f'Division entera {100 // 2}')
print(f'Exponencial {3**2}')
print(f'Modulo {25 % 2}')